"""
Calculater Functions
"""


def add(x: int | float, y: int | float) -> int | float:
    """Add x and y and return result

    Args:
        x (int | float): The first number
        y (int | float): The second number

    Returns:
        int|float: The result of x + y
    """
    return x + y


def substract(x: int | float, y: int | float) -> int | float:
    """Substract x from y and return result

    Args:
        x (int | float): The number to substract
        y (int | float): The number from which something will be substracted

    Returns:
        int|float: The result of y - x
    """
    return y - x
